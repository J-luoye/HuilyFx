using System.ComponentModel;

namespace HuilyFx.St.Common.Extends
{
    /// <summary>
    /// 常用时间格式
    /// </summary>
    public class TimeFromat
    {
        /// <summary>
        /// yyyyMMdd
        /// </summary>
        public const string NUMDATE = "yyyyMMdd";
        /// <summary>
        /// yyyy-MM-dd
        /// </summary>
        public const string STRIKEDATE = "yyyy-MM-dd";
        /// <summary>
        /// yyyyMMddHHmmss
        /// </summary>
        public const string NUMDATETIME = "yyyyMMddHHmmss";
        /// <summary>
        /// yyyyMMdd
        /// </summary>
        public const string TWOYEARNUMDATETIME = "yyyyMMdd";
        /// <summary>
        /// yyyy-MM-dd HH:mm:ss SSS
        /// </summary>
        public const string MILLISECONDTIME = "yyyy-MM-dd HH:mm:ss SSS";
        /// <summary>
        /// HHmmss
        /// </summary>
        public const string NUMTIME = "HHmmss";
        /// <summary>
        /// yyyy-MM-dd 00:00:00
        /// </summary>
        public const string BEGIN = "yyyyMMdd 00:00:00";
        /// <summary>
        /// yyyy-MM-dd 23:59:59
        /// </summary>
        public const string END = "yyyy-MM-dd 23:59:59";
        /// <summary>
        /// yyyy-MM-dd HH:mm
        /// </summary>
        public const string STRIKEDATETIMETOMIN = "yyyy-MM-dd HH:mm";
        /// <summary>
        /// yyyy-MM-dd HH:mm:ss
        /// </summary>
        public const string STRIKEDATETIMESEC = "yyyy-MM-dd HH:mm:ss";
        /// <summary>
        /// yyyy年MM月dd日
        /// </summary>
        public const string STRIKEDATE_CH = "yyyy年MM月dd日";
        /// <summary>
        /// yyyy年MM月dd日 HH:mm
        /// </summary>
        public const string STRIKEDATETIMEMIN_CH = "yyyy年MM月dd日 HH:mm";
        /// <summary>
        /// yyyy年MM月dd日 HH:mm:ss
        /// </summary>
        public const string STRIKEDATETIMESEC_CH = "yyyy年MM月dd日 HH:mm:ss";
    }
}
